package org.example.webapp.validation.user.model;

import jakarta.validation.constraints.NotNull;

public class User {

    @NotNull
    private String name;

    private Integer age;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }
}
